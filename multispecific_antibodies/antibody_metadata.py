def is_multispecific(antibody_name):
    """Check if antibody is multispecific"""
    if antibody_name not in ANTIBODY_FORMATS:
        return None
    return ANTIBODY_FORMATS[antibody_name]["is_multispecific"]

def get_format(antibody_name):
    """Get format type for antibody"""
    if antibody_name not in ANTIBODY_FORMATS:
        return None
    return ANTIBODY_FORMATS[antibody_name]["format"]

def get_antibodies_by_format(format_name):
    """Get all antibodies of a specific format"""
    return FORMAT_GROUPS.get(format_name, [])
